    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        try {
            cuad1.setLado(Integer.parseInt(txf_lado.getText()));
            isNum = true;
        } catch (NumberFormatException e) {
            isNum = false;
            JOptionPane.showMessageDialog(null, "Error, ingrese un número");
        }

        if (isNum) {
            if (cuad1.getLado() > 0 && cuad1.getLado() < 300) {
                if (Double.parseDouble(txf_lado.getText()) % 1 == 0) {
                    ValidarYGraficar();
                } else {
                    JOptionPane.showMessageDialog(null, "Ingrese un lado entero");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Ingrese un lado entre 0 y 300.");
            }

        }
    }//GEN-LAST:event_btnCalcularActionPerformed

