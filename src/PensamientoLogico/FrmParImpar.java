    public void actualizarBits() {
        LabelBits.setText(String.valueOf(ListaUsuarios.matrizUsuarios[ListaUsuarios.posUsuario].getBits()));
    }

    public void crearnumeros() {
        Lita = new ListaInt();
        nod = Lita.inicio;
        JButton numeros[] = {btn_1, btn_2, btn_3, btn_4, btn_5, btn_6, btn_7, btn_8, btn_9};
        for (int i = 0; i < 9; i++) {
            numeros[i].setText(nod.dato + "");
            nod = nod.siguiente;
        }
    }

    public void alternarniveles() {
        Random random = new Random();
        int numeroAleatorio = random.nextInt(2);

        if (numeroAleatorio == 0) {
            this.lbl_par.setVisible(true);
            espar = true;

        } else {
            espar = false;
            this.lbl_impar.setVisible(true);
        }
    }

    public void backgroundblanco() {
        boton = listaBotones.inicio;
        for (int i = 0; i < 9; i++) {
            boton.dato.setBackground(Color.white);
            boton.dato.setForeground(Color.black);
            boton = boton.siguiente;
        }
    }

