    private void actualizarExtrasTxf() {
        lbl_per.setText(cuad1.per()+" cm");
        lbl_area.setText(cuad1.area()+" cm^2");
    }

    private void ValidarYGraficar() {
        Grafico.dibujarCuadrado(PanelGrafico.getGraphics(), centro, ladoAnt, bg);

        centro.x = PanelGrafico.getWidth() / 2;
        centro.y = PanelGrafico.getHeight() / 2;
        centro.x -= cuad1.getLado() / 2;
        centro.y -= cuad1.getLado() / 2;

        Grafico.dibujarCuadrado(PanelGrafico.getGraphics(), centro, cuad1.getLado(), Color.black);

        ladoAnt = cuad1.getLado();

        actualizarExtrasTxf();
    }

