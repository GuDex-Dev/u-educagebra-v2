    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        boton = listaBotones.inicio;
        for (int i = 0; i < 9; i++) {
            if (espar == true) {
                if (boton.dato.getBackground().equals(Color.gray)) {
                    if (Integer.parseInt(boton.dato.getText()) % 2 == 0) {
                        boton.dato.setBackground(Color.green);
                        boton.dato.setForeground(Color.black);
                    } else {
                        boton.dato.setBackground(Color.red);
                        boton.dato.setForeground(Color.white);
                    }
                } else {
                    if (Integer.parseInt(boton.dato.getText()) % 2 != 0) {
                        boton.dato.setBackground(Color.green);
                        boton.dato.setForeground(Color.black);
                    } else {
                        boton.dato.setBackground(Color.red);
                        boton.dato.setForeground(Color.white);
                    }
                }
            } else {
                if (boton.dato.getBackground().equals(Color.gray)) {
                    if (Integer.parseInt(boton.dato.getText()) % 2 != 0) {
                        boton.dato.setBackground(Color.green);
                        boton.dato.setForeground(Color.black);
                    } else {
                        boton.dato.setBackground(Color.red);
                        boton.dato.setForeground(Color.white);
                    }
                } else {
                    if (Integer.parseInt(boton.dato.getText()) % 2 == 0) {
                        boton.dato.setBackground(Color.green);
                        boton.dato.setForeground(Color.black);
                    } else {
                        boton.dato.setBackground(Color.red);
                        boton.dato.setForeground(Color.white);
                    }
                }
            }
            boton = boton.siguiente;
        }
    }//GEN-LAST:event_btnCalcularActionPerformed

